function makeOffer(
  id: string,
  storeKey: StoreKey,
  storeName: string,
  productName: string,
  price: number,
  mrp: number | undefined,
  url: string,
  isAffiliate: boolean,
  isSponsored: boolean
) {
  const discountPercent =
    mrp && mrp > price ? Math.round(((mrp - price) / mrp) * 100) : undefined;
  return {
    id,
    storeKey,
    storeName,
    productName,
    price,
    mrp,
    discountPercent,
    url,
    isAffiliate,
    isSponsored,
    availabilityStatus: "available" as const,
    lastCheckedText: "Listed in AuraCheck catalog",
    updatedAt: now,
  };
}

